public class AdapterListenerPaper extends ZUtils implements Listener {

    private final SpawnerPlugin plugin;

    public AdapterListenerPaper(SpawnerPlugin plugin) {
        this.plugin = plugin;
    }

    @EventHandler(priority = EventPriority.HIGHEST)
    public void onKnockBack(EntityKnockbackByEntityEvent event) {
        if (event.isCancelled()) return;

        this.plugin.getListenerAdapters().forEach(adapter -> adapter.onKnockBack(event, event.getEntity()));
    }

}
